public class day2 {
    
    public static void main(String[] args) {
        System.out.println(2 * 2);            // Multiplication
            System.out.println(5 / 3);        // Integer division
            System.out.println(5.0 / 3);      // Decimal division
            System.out.println(7 % 3);        // Modulus (remainder)
            System.out.println(10 - 4);       // Subtraction
            System.out.println(8 + 2);        // Addition
            System.out.println(3 * (4 + 2));  // Parentheses to change order
            System.out.println(10 / 4.0);     // Division with one operand as double
    }
        
}
